var firstNumber = 46;
var secondNumber = 10;

// JavaScript operators:

// 1 for collect numbers and combine texts, use + , Example:
   var sum = firstNumber + secondNumber;
   console.log("result is " + sum);

// 2 for subtraction, use - , Example:
   var sum = firstNumber - secondNumber;

// 3 for multiplication, use * , Example:
   var sum = firstNumber * secondNumber;

// 4 for division, use / , Example:
   var sum = firstNumber / secondNumber;

// 5 for the remainder of division, use % , Example:
   var sum = firstNumber % secondNumber;

// 6 for exponentiation, use ** , Example:
   var sum = firstNumber ** secondNumber;

// 7 for incrementing one, use ++ , Example:
   secondNumber++;

// 8 for decrementing one, use -- , Example:
   secondNumber--;

// 9 for express 'and', use && , Example:
   var result = 5 < 7 && 9 < 6;

// 10 for express 'or', use || , Example:
   var result = 5 < 7 || 9 < 6;



